/*문제 2
A에 2저장
B에 3저장
변수 두개 선언 재선언 x
10을 출력하는 프로그램이다.
*/

// 클래스 선언, 클래스 이름 test2
public class test2 {

	// 메소드 선언(public), 정적 메소드(static), 값을 리턴하지 않음(void), 메소드 이름(main)
	public static void main(String[] args) {
		
		int A = 2; // 변수 A선언 2저장
		int B = 3; // 변수 B선언 3저장
		
		System.out.println((A+B) * 2); // A+B 더하고 2곱한 값 출력

	} // End main

} // End class
