/*문제 1
변수 x에 5저장 변수 y에 10저장
z를 x와 z의 합으로 저장
z 값 출력 
*/

// 클래스 선언, 클래스 이름 test1
public class test1 {

	// 메소드 선언(public), 정적 메소드(static), 값을 리턴하지 않음(void), 메소드 이름(main)
	public static void main(String[] args) {
		
		int x = 5; // 정수형 변수 x 저장 5저장
		int y = 10; // 정수형 변수 y선언 10저장
		int z = x + y; // 정수형 z 선언 x+y값 저장
		
		System.out.println(z); // z 값 출력
	
	} // End main

} // End class
