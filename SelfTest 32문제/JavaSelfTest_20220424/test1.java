/*문제 1
x에 k 저장
y에 g 저장
아스키코드 값 출력
*/

// 클래스 선언, 클래스 이름 test1
public class test1 {

	// 메소드 선언(public), 정적 메소드(static), 값을 리턴하지 않음(void), 메소드 이름(main)
	public static void main(String[] args) {
		
		char x = 'k'; // x변수 선언 k저장
		char y = 'g'; // y변수 선언 g 저장
		
		System.out.printf("%d %d",(int)x,(int)y); // 아스키코드값 출력

	}  // End main

} // End class
